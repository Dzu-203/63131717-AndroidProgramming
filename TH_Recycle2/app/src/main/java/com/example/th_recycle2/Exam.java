package com.example.th_recycle2;

public class Exam {
    String name;
    String date;
    String message;

    public Exam(String name, String date, String message) {
        this.name = name;
        this.date = date;
        this.message = message;
    }

    public String getName() {
        return name;
    }

    public String getDate() {
        return date;
    }

    public String getMessage() {
        return message;
    }
}
