package com.example.myapplication;

public class Food {
    String imgFood;
    String contextFood;

    public Food(String imgFood, String contextFood) {
        this.imgFood = imgFood;
        this.contextFood = contextFood;
    }

    public String getImgFood() {
        return imgFood;
    }

    public String getContextFood() {
        return contextFood;
    }

}
